        private static void ShowIPAddresses(IPInterfaceProperties properties)
        {
            Console.WriteLine("  Anycase Address ......................... : {0}",
                String.Join(", ", properties.AnycastAddresses.Select(a => a.Address.ToString())));
            Console.WriteLine("  DHCP Server Address ..................... : {0}",
                String.Join(", ", properties.DhcpServerAddresses.Select(a => a.ToString())));
            Console.WriteLine("  DNS Server Address ...................... : {0}",
                String.Join(", ", properties.DnsAddresses.Select(a => a.ToString())));
        }

        public static void DumpDns()
        {
            Console.WriteLine("  Networking Available .................... : {0}",
                NetworkInterface.GetIsNetworkAvailable());
            
            var nics = NetworkInterface.GetAllNetworkInterfaces()
                .Where(nic => nic.OperationalStatus == OperationalStatus.Up
                    && nic.NetworkInterfaceType != NetworkInterfaceType.Loopback
                    && !nic.IsReceiveOnly);
            foreach (var nic in nics) {
                var props = nic.GetIPProperties();
                if (!props.DnsAddresses.Any()) continue;
                Console.WriteLine("  NIC Id .................................. : {0}",
                    nic.Id);
                Console.WriteLine("  DNS Server Address ...................... : {0}",
                    String.Join(", ", nic.GetIPProperties().DnsAddresses.Select(a => a.ToString())));
            }
        }
